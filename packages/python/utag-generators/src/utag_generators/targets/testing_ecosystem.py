@register_generator("mock-server")
class MockServerGenerator:
    def generate(self, module: ModuleSpec) -> dict[str, str]:
        lines = ["// generated by utag — mock server"]
        lines.append('const express = require("express");')
        lines.append('const app = express();')
        lines.append('app.use(express.json());')
        lines.append("")
        
        for op in module.operations:
            method = op.method.lower()
            express_path = op.path
            for p in op.request.path_params:
                express_path = express_path.replace(f"{{{p.name}}}", f":{p.name}")
                
            lines.append(f"app.{method}('{express_path}', (req, res) => {{")
            lines.append("  // Mock implementation")
            expected_status = 200
            if op.responses:
                expected_status = op.responses[0].status_code
                
            lines.append(f"  res.status({expected_status}).json({{}});")
            lines.append("});")
            lines.append("")
            
        lines.append("const PORT = process.env.PORT || 3000;")
        lines.append("app.listen(PORT, () => {")
        lines.append(f"  console.log('{module.name} mock server listening on port ' + PORT);")
        lines.append("});")
        
        return {f"{module.name}-mock.js": "\n".join(lines) + "\n"}

@register_generator("wiremock-stubs")
class WiremockStubsGenerator:
    def generate(self, module: ModuleSpec) -> dict[str, str]:
        import json
        mappings = []
        for op in module.operations:
            url_path = op.path
            # Replace OpenAPI {param} with Wiremock matching or exact values
            # Wiremock prefers URL Path matching
            for p in op.request.path_params:
                url_path = url_path.replace(f"{{{p.name}}}", f"test_{p.name}")
                
            request = {
                "method": op.method.upper(),
                "urlPath": url_path
            }
            
            if op.request.query_params:
                request["queryParameters"] = {
                    q.name: {"equalTo": f"test_{q.name}"} for q in op.request.query_params
                }
                
            expected_status = 200
            if op.responses:
                expected_status = op.responses[0].status_code
                
            response = {
                "status": expected_status,
            }
            if op.responses and op.responses[0].body_type:
                response["headers"] = {"Content-Type": "application/json"}
                response["jsonBody"] = {}
                
            mappings.append({
                "request": request,
                "response": response
            })
            
        out = {"mappings": mappings}
        return {f"{module.name}-wiremock.json": json.dumps(out, indent=2) + "\n"}

