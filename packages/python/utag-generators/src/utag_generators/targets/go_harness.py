EMBED_GO = """// Package embedded bakes utag-generated artifacts into the binary (go:embed).
package embedded

import "embed"

//go:embed artifacts/*
var FS embed.FS
"""


@register_generator("go-harness")
class GoHarnessGenerator:
    def generate(self, module: ModuleSpec) -> dict[str, str]:
        # composition: embed sibling generators' deterministic artifacts
        from utag_core.registry import get_generator
        name = module.name
        kebab = name.replace("_", "-")
        embedded = {}
        for sibling in ("openapi-3.1", "prompt-yaml"):
            for rel, content in get_generator(sibling).generate(module).items():
                embedded[f"embedded/artifacts/{rel}"] = content
        files = {
            "go.mod": GO_MOD.format(name=name, cobra=COBRA_V, viper=VIPER_V),
            "main.go": MAIN_GO.format(name=name),
            "config/config.go": CONFIG_GO,
            "cmd/root.go": ROOT_GO.format(name=name, kebab=kebab),
            "cmd/config.go": CONFIG_CMD_GO,
            "cmd/artifacts.go": ARTIFACTS_CMD_GO.format(name=name),
            "embedded/embed.go": EMBED_GO,
        }
        files.update(embedded)
        return files
