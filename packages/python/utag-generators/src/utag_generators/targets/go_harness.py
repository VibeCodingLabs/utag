CONFIG_GO = """// Package config: ONE struct describes all settings; viper fills it automatically.
package config

import "github.com/spf13/viper"

// Settings — single source of truth. Precedence (viper): explicit Set > flag >
// env (UTAG_*) > config file > default. Twelve-factor III: env-first config.
type Settings struct {
\tLogLevel    string `mapstructure:"log-level" json:"log-level"`
\tPort        int    `mapstructure:"port" json:"port"`
\tProvider    string `mapstructure:"provider" json:"provider"`
\tEndpoint    string `mapstructure:"endpoint" json:"endpoint"`
\tMaxAttempts int    `mapstructure:"max-attempts" json:"max-attempts"`
}

// Defaults registers every default in one place.
func Defaults(v *viper.Viper) {
\tv.SetDefault("log-level", "info")
\tv.SetDefault("port", 8080)
\tv.SetDefault("provider", "anthropic")
\tv.SetDefault("endpoint", "")
\tv.SetDefault("max-attempts", 3)
}

// Load = "read all my settings and fill this struct automatically."
func Load(v *viper.Viper) (Settings, error) {
\tvar s Settings
\terr := v.Unmarshal(&s) // decoding via go-viper/mapstructure/v2
\treturn s, err
}
"""

ROOT_GO = """package cmd

import (
\t"fmt"
\t"os"
\t"strings"

\t"github.com/spf13/cobra"
\t"github.com/spf13/viper"

\t"utag-harness/{name}/config"
)

var (
\tv        = viper.New()
\tsettings config.Settings
\tcfgFile  string
)

var rootCmd = &cobra.Command{{
\tUse:   "{kebab}",
\tShort: "{kebab} — utag-generated agent harness / control-plane",
\tLong:  "Single-binary control-plane generated by utag. Settings: one struct, filled by viper (defaults < config < env UTAG_* < flags).",
}}

func Execute() {{
\tif err := rootCmd.Execute(); err != nil {{
\t\tfmt.Fprintln(os.Stderr, err)
\t\tos.Exit(1)
\t}}
}}

func init() {{
\tcobra.OnInitialize(initConfig)
\trootCmd.PersistentFlags().StringVar(&cfgFile, "config", "", "config file (default ./utag.yaml)")
\trootCmd.PersistentFlags().String("log-level", "info", "log level")
\trootCmd.PersistentFlags().Int("port", 8080, "control-plane port")
\trootCmd.PersistentFlags().String("provider", "anthropic", "model provider")
\trootCmd.PersistentFlags().Int("max-attempts", 3, "bounded repair attempts")
\t_ = v.BindPFlags(rootCmd.PersistentFlags())
}}

func initConfig() {{
\tconfig.Defaults(v)
\tif cfgFile != "" {{
\t\tv.SetConfigFile(cfgFile)
\t}} else {{
\t\tv.SetConfigName("utag")
\t\tv.SetConfigType("yaml")
\t\tv.AddConfigPath(".")
\t}}
\tv.SetEnvPrefix("UTAG")
\tv.SetEnvKeyReplacer(strings.NewReplacer("-", "_", ".", "_"))
\tv.AutomaticEnv()
\t// twelve-factor: env overrides file; config file optional by design
\t_ = v.ReadInConfig()
\tvar err error
\tif settings, err = config.Load(v); err != nil {{
\t\tfmt.Fprintln(os.Stderr, "config:", err)
\t\tos.Exit(1)
\t}}
}}
"""

